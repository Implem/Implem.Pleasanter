        /// <summary>
        /// Fixed:
        /// </summary>
        private void SetByFormData(Context context, SiteSettings ss, Dictionary<string, string> formData)
        {
            formData.ForEach(data =>
            {
                var key = data.Key;
                var value = data.Value ?? string.Empty;
                switch (key)
                {
                    case "Parameters_Title": Title = new Title(ParameterId, value); break;
                    case "Parameters_Body":
                        var json_default = Parameters.ParameterHash.Get(Title.Value);
                        var json_modified = context.Forms.Data("Parameters_Body");
                        if (json_default.IsNullOrEmpty()
                            || json_modified.IsNullOrEmpty())
                        {
                            break;
                        }
                        if (Title.Value == "Security")
                        {
                            json_modified = Jsons.MergeArrayUnion(
                                original: json_default,
                                target: json_modified,
                                propertyName: "PrivilegedUsers");
                        }
                        var patch = json_default == json_modified
                            ? string.Empty
                            : Jsons.BuildPatch(
                                a: json_default,
                                b: json_modified,
                                typeName: Title.Value);
                        Body = patch == "[]"
                            ? string.Empty
                            : patch;
                        break;
                    case "Parameters_Timestamp": Timestamp = value.ToString(); break;
                    case "Comments": Comments.Prepend(
                        context: context,
                        ss: ss,
                        body: value); break;
                    case "VerUp": VerUp = value.ToBool(); break;
                    default:
                        if (key.RegexExists("Comment[0-9]+"))
                        {
                            Comments.Update(
                                context: context,
                                ss: ss,
                                commentId: key.Substring("Comment".Length).ToInt(),
                                body: value);
                        }
                        else
                        {
                            var column = ss.GetColumn(
                                context: context,
                                columnName: key.Split_2nd('_'));
                            switch (Def.ExtendedColumnTypes.Get(column?.ColumnName ?? string.Empty))
                            {
                                case "Class":
                                    SetClass(
                                        columnName: column.ColumnName,
                                        value: value);
                                    break;
                                case "Num":
                                    SetNum(
                                        columnName: column.ColumnName,
                                        value: new Num(
                                            context: context,
                                            column: column,
                                            value: value));
                                    break;
                                case "Date":
                                    SetDate(
                                        columnName: column.ColumnName,
                                        value: value.ToDateTime().ToUniversal(context: context));
                                    break;
                                case "Description":
                                    SetDescription(
                                        columnName: column.ColumnName,
                                        value: Implem.Pleasanter.Models.BinaryUtilities.NormalizeFormBinaryPath(context, value.ToString()));
                                    break;
                                case "Check":
                                    SetCheck(
                                        columnName: column.ColumnName,
                                        value: value.ToBool());
                                    break;
                                case "Attachments":
                                    SetAttachments(
                                        columnName: column.ColumnName,
                                        value: value.Deserialize<Attachments>());
                                    break;
                            }
                        }
                        break;
                }
            });
        }

